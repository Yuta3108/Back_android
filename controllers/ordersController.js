exports.getAllOrders = (req, res) => {
    const sql = `
        SELECT 
            d.madonhang,
            d.ngaydat,
            d.tongtien,
            d.trangthai,
            d.ghichu,
            d.phuongthucthanhtoan,
            u.id AS user_id,
            u.name AS ten_khach_hang,
            p.id AS product_id,
            p.name AS ten_san_pham,
            ctdh.soluong
        FROM donhang d
        JOIN chitietdonhang ctdh ON d.madonhang = ctdh.madonhang
        JOIN products p ON ctdh.masanpham = p.id
        JOIN users u ON d.mauser = u.id
        ORDER BY d.ngaydat DESC
    `;

    db.query(sql, (err, results) => {
        if (err) {
            console.error('‚ùå L·ªói khi l·∫•y t·∫•t c·∫£ ƒë∆°n h√†ng:', err);
            return res.status(500).json({ message: 'L·ªói server' });
        }

        if (results.length === 0) {
            return res.status(404).json({ message: 'Kh√¥ng c√≥ ƒë∆°n h√†ng n√†o' });
        }

        // Gom l·∫°i theo ƒë∆°n h√†ng
        const ordersMap = {};

        results.forEach(row => {
            const {
                madonhang,
                ngaydat,
                tongtien,
                trangthai,
                ghichu,
                phuongthucthanhtoan,
                user_id,
                ten_khach_hang,
                product_id,
                ten_san_pham,
                soluong
            } = row;

            if (!ordersMap[madonhang]) {
                ordersMap[madonhang] = {
                    madonhang,
                    ngaydat,
                    tongtien,
                    trangthai,
                    ghichu,
                    phuongthucthanhtoan,
                    user: {
                        id: user_id,
                        name: ten_khach_hang
                    },
                    chitiet: []
                };
            }

            ordersMap[madonhang].chitiet.push({
                product_id,
                ten_san_pham,
                soluong
            });
        });

        const orders = Object.values(ordersMap);
        res.json(orders);
    });
};


